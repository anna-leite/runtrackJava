package JOUR01;

public class Job0 {
    public static void Variables() {
        char var1 = 'A';
        System.out.println("char variable : " + var1);

        String var2 = "Hello world!";
        System.out.println("string variable : " + var2);

        int var3 = 2;
        System.out.println("int variable : " + var3);

        long var4 = 9223372036854775L;
        System.out.println("long variable : " + var4);

        float var5 = 1.2f;
        System.out.println("float variable : " + var5);

        boolean var6 =  true;
        System.out.println("boolean variable : " + var6);

        int TOTO = (int)3.817f;
        System.out.println("TOTO variable : " + TOTO);
    }
}
